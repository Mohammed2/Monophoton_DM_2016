for sample_name in DM_NLOfromInterpolation_samples_and_multipliers.keys():
  # Get scaled r values from input file
  observed_r = -1.0
  expected50_r = -1.0
  expected16_r = -1.0
  expected84_r = -1.0
  expected2p5_r = -1.0
  expected97p5_r = -1.0
  for line in open(infolder+'/scaled_limits_'+sample_name+'.txt').readlines():
    if line[:len('Observed Limit')] == 'Observed Limit':
      observed_r = float(line.strip().split()[-1])
    elif line[:len('Expected 2')] == 'Expected 2':
      expected2p5_r = float(line.strip().split()[-1])
    elif line[:len('Expected 16')] == 'Expected 16':
      expected16_r = float(line.strip().split()[-1])
    elif line[:len('Expected 50')] == 'Expected 50':
      expected50_r = float(line.strip().split()[-1])
    elif line[:len('Expected 84')] == 'Expected 84':
      expected84_r = float(line.strip().split()[-1])
    elif line[:len('Expected 97')] == 'Expected 97':
      expected97p5_r = float(line.strip().split()[-1])
  # Unscale r values
  true_observed_r = observed_r * float(DM_NLOfromInterpolation_samples_and_multipliers[sample_name])
  true_observed_plus20_r = true_observed_r * 1.20
  true_observed_minus20_r = true_observed_r * 0.80
  true_expected50_r = expected50_r * float(DM_NLOfromInterpolation_samples_and_multipliers[sample_name])
  true_expected16_r = expected16_r * float(DM_NLOfromInterpolation_samples_and_multipliers[sample_name])
  true_expected84_r = expected84_r * float(DM_NLOfromInterpolation_samples_and_multipliers[sample_name])
  true_expected2p5_r = expected2p5_r * float(DM_NLOfromInterpolation_samples_and_multipliers[sample_name])
  true_expected97p5_r = expected97p5_r * float(DM_NLOfromInterpolation_samples_and_multipliers[sample_name])
  # Write unscaled ('true') r values to output files
  shortname = ''.join(sample_name.split('_'))
  labels_and_true_observed_r_all_NLOfromInterpolation.write(shortname+' '+str(true_observed_r)+'\n')
  labels_and_true_expected50_r_all_NLOfromInterpolation.write(shortname+' '+str(true_expected50_r)+'\n')
  labels_and_true_expected16_r_all_NLOfromInterpolation.write(shortname+' '+str(true_expected16_r)+'\n')
  labels_and_true_expected84_r_all_NLOfromInterpolation.write(shortname+' '+str(true_expected84_r)+'\n')
  labels_and_true_expected2p5_r_all_NLOfromInterpolation.write(shortname+' '+str(true_expected2p5_r)+'\n')
  labels_and_true_expected97p5_r_all_NLOfromInterpolation.write(shortname+' '+str(true_expected97p5_r)+'\n')
  labels_and_true_observed_plus20_r_all_NLOfromInterpolation.write(shortname+' '+str(true_observed_plus20_r)+'\n')
  labels_and_true_observed_minus20_r_all_NLOfromInterpolation.write(shortname+' '+str(true_observed_minus20_r)+'\n')
